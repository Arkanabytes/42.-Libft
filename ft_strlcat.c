#include "libft.h"

char			*ft_strcat(char *s1, const char *s2)
{
	int			late;
	int			i;

	i = 0;
	late = ft_strlen(s1);
	while (s2[i] != 0)
	{
		s1[late++] = s2[i++];
	}
	s1[late] = '\0';
	return (s1);
}
