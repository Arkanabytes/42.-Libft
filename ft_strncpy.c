char		*ft_strncpy(char *dest, char *src, unsigned int n)
{
	int		i;
	char	*temp;

	temp = dest;
	i = 0;
	while (i <  n)
	{
		while (src[i] != '\0')
		{
			dest[i] = '\0';
			i++;
		}
	}
	return (dest);
}
